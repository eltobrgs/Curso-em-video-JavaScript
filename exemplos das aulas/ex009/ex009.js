// Objetivo: Receber um país e informar se é Brasileiro ou Estrangeiro.

// Definindo a variável "pais" e atribuindo o valor "Brasil"
var pais = "Brasil";

// Imprimindo a mensagem "Vivendo em" seguida do valor da variável "pais"
console.log(`Vivendo em ${pais}`);

// Verificando se o valor da variável "pais" é igual a "Brasil"
if (pais == "Brasil") {
    // Se for igual, imprime a mensagem "Você é Brasileiro"
    console.log("Você é Brasileiro");
} else {
    // Se não for igual, imprime a mensagem "Você é Estrangeiro"
    console.log("Você é Estrangeiro");
}